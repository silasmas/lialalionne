<h2>Politique de retour</h2>
<p>
  Vous disposez de <strong>14 jours</strong> après réception pour retourner un produit non ouvert
  et dans son emballage d'origine.
</p>

<h2>Produits exclus</h2>
<p>
  Pour des raisons d'hygiène, les produits descellés, utilisés ou ouverts ne peuvent être retournés.
</p>

<h2>Procédure</h2>
<ol>
  <li>Contactez-nous à contact@lialalionne.com avec votre numéro de commande</li>
  <li>Recevez l'adresse de retour et les instructions d'emballage</li>
  <li>Expédiez le colis — les frais de retour sont à votre charge sauf produit défectueux</li>
  <li>Remboursement sous 14 jours après réception et contrôle du retour</li>
</ol>

<h2>Produit défectueux</h2>
<p>
  En cas de produit endommagé ou défectueux à la réception, contactez-nous sous 48 h avec photos.
  Nous organiserons un échange ou remboursement intégral, frais de retour pris en charge.
</p>
